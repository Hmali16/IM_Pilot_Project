CREATE OR REPLACE VIEW META.SILVER.V_DATA_QUALITY_SUMMARY AS
SELECT 'SLV_AD_ACCOUNT' AS TABLE_NAME, COUNT(*) AS TOTAL_ROWS,
    SUM(CASE WHEN IS_VALID_RECORD AND NOT IS_DELETED THEN 1 ELSE 0 END) AS VALID_ACTIVE_ROWS,
    NULL AS VALID_EMAILS,
    SUM(CASE WHEN IS_DELETED THEN 1 ELSE 0 END) AS DELETED_ROWS,
    ROUND(SUM(CASE WHEN IS_VALID_RECORD THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2) AS VALIDITY_PCT
FROM META.SILVER.SLV_AD_ACCOUNT
UNION ALL
SELECT 'SLV_CAMPAIGN', COUNT(*),
    SUM(CASE WHEN IS_VALID_RECORD AND NOT IS_DELETED THEN 1 ELSE 0 END), NULL,
    SUM(CASE WHEN IS_DELETED THEN 1 ELSE 0 END),
    ROUND(SUM(CASE WHEN IS_VALID_RECORD THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2)
FROM META.SILVER.SLV_CAMPAIGN
UNION ALL
SELECT 'SLV_ADSET', COUNT(*),
    SUM(CASE WHEN IS_VALID_RECORD AND NOT IS_DELETED THEN 1 ELSE 0 END), NULL,
    SUM(CASE WHEN IS_DELETED THEN 1 ELSE 0 END),
    ROUND(SUM(CASE WHEN IS_VALID_RECORD THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2)
FROM META.SILVER.SLV_ADSET
UNION ALL
SELECT 'SLV_AD', COUNT(*),
    SUM(CASE WHEN IS_VALID_RECORD AND NOT IS_DELETED THEN 1 ELSE 0 END), NULL,
    SUM(CASE WHEN IS_DELETED THEN 1 ELSE 0 END),
    ROUND(SUM(CASE WHEN IS_VALID_RECORD THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2)
FROM META.SILVER.SLV_AD
UNION ALL
SELECT 'SLV_CUSTOM_AUDIENCE', COUNT(*),
    SUM(CASE WHEN IS_VALID_RECORD AND NOT IS_DELETED THEN 1 ELSE 0 END), NULL,
    SUM(CASE WHEN IS_DELETED THEN 1 ELSE 0 END),
    ROUND(SUM(CASE WHEN IS_VALID_RECORD THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2)
FROM META.SILVER.SLV_CUSTOM_AUDIENCE
UNION ALL
SELECT 'SLV_INSIGHT_AD', COUNT(*),
    SUM(CASE WHEN IS_VALID_RECORD AND IS_VALID_DATE_RANGE AND NOT IS_DELETED THEN 1 ELSE 0 END),
    SUM(CASE WHEN IS_VALID_DATE_RANGE THEN 1 ELSE 0 END),
    SUM(CASE WHEN IS_DELETED THEN 1 ELSE 0 END),
