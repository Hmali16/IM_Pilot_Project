-- -----------------------------------------------------------------------------
-- MERGE_SLV_AD_ACCOUNT
-- -----------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE META.SILVER.MERGE_SLV_AD_ACCOUNT()
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
BEGIN
    MERGE INTO META.SILVER.SLV_AD_ACCOUNT AS tgt
    USING (
        SELECT
            ACCOUNT_ID,
            ID                              AS ACCOUNT_ID_TEXT,
            TRIM(NAME)                      AS ACCOUNT_NAME,
            ACCOUNT_STATUS,
            TRIM(CURRENCY)                  AS CURRENCY,
            TRIM(TIMEZONE_NAME)             AS TIMEZONE_NAME,
            SPEND_CAP, AMOUNT_SPENT, BALANCE, BUSINESS_ID,
            TRIM(BUSINESS_NAME)             AS BUSINESS_NAME,
            CASE WHEN ACCOUNT_ID IS NULL THEN FALSE ELSE TRUE END AS IS_VALID_RECORD,
            TRY_TO_TIMESTAMP_NTZ(AUDIT_INSERTED_AT) AS AUDIT_INSERTED_AT,
            TRY_TO_TIMESTAMP_NTZ(AUDIT_UPDATED_AT)  AS AUDIT_UPDATED_AT,
            AUDIT_IS_DELETED                AS IS_DELETED,
            BATCH_ID
        FROM META.BRONZE.STREAM_META_AD_ACCOUNT
        QUALIFY ROW_NUMBER() OVER (PARTITION BY ACCOUNT_ID ORDER BY AUDIT_UPDATED_AT DESC NULLS LAST) = 1
    ) AS src
    ON tgt.ACCOUNT_ID = src.ACCOUNT_ID
    WHEN MATCHED AND src.IS_DELETED = TRUE THEN
        UPDATE SET tgt.IS_DELETED = TRUE, tgt.SILVER_UPDATED_AT = CURRENT_TIMESTAMP()
    WHEN MATCHED AND (tgt.AUDIT_UPDATED_AT < src.AUDIT_UPDATED_AT OR tgt.AUDIT_UPDATED_AT IS NULL) THEN
        UPDATE SET
            tgt.ACCOUNT_ID_TEXT = src.ACCOUNT_ID_TEXT, tgt.ACCOUNT_NAME = src.ACCOUNT_NAME,
            tgt.ACCOUNT_STATUS = src.ACCOUNT_STATUS, tgt.CURRENCY = src.CURRENCY,
            tgt.TIMEZONE_NAME = src.TIMEZONE_NAME, tgt.SPEND_CAP = src.SPEND_CAP,
            tgt.AMOUNT_SPENT = src.AMOUNT_SPENT, tgt.BALANCE = src.BALANCE,
            tgt.BUSINESS_ID = src.BUSINESS_ID, tgt.BUSINESS_NAME = src.BUSINESS_NAME,
            tgt.IS_VALID_RECORD = src.IS_VALID_RECORD, tgt.AUDIT_UPDATED_AT = src.AUDIT_UPDATED_AT,
            tgt.IS_DELETED = src.IS_DELETED, tgt.BATCH_ID = src.BATCH_ID,
            tgt.SILVER_UPDATED_AT = CURRENT_TIMESTAMP()
    WHEN NOT MATCHED AND src.IS_DELETED = FALSE THEN
        INSERT (ACCOUNT_ID, ACCOUNT_ID_TEXT, ACCOUNT_NAME, ACCOUNT_STATUS, CURRENCY,
                TIMEZONE_NAME, SPEND_CAP, AMOUNT_SPENT, BALANCE, BUSINESS_ID, BUSINESS_NAME,
                IS_VALID_RECORD, AUDIT_INSERTED_AT, AUDIT_UPDATED_AT, IS_DELETED, BATCH_ID)
        VALUES (src.ACCOUNT_ID, src.ACCOUNT_ID_TEXT, src.ACCOUNT_NAME, src.ACCOUNT_STATUS,
                src.CURRENCY, src.TIMEZONE_NAME, src.SPEND_CAP, src.AMOUNT_SPENT, src.BALANCE,
                src.BUSINESS_ID, src.BUSINESS_NAME, src.IS_VALID_RECORD,
                src.AUDIT_INSERTED_AT, src.AUDIT_UPDATED_AT, src.IS_DELETED, src.BATCH_ID);
    RETURN 'MERGE_SLV_AD_ACCOUNT completed at ' || CURRENT_TIMESTAMP()::VARCHAR;
END;
$$;

